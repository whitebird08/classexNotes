// function declartion 
function sumDeclartion(arg1, arg2){
  console.log(arg1 + arg2);
}
sumDeclartion(1,2);


// function expression
var sumExpression = function(arg1, arg2) {
  console.log(arg1 + arg2);
}
sumExpression(1,2);


// IIFE 
(function (arg1,arg2){
  console.log(arg1 + arg2);
})(1, 2); 






